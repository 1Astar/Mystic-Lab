const minors = [];
for (const suitKey of Object.keys(SUIT)) {
  for (const rankKey of Object.keys(RANK)) {
    const card = buildMinorCard(suitKey, rankKey);
    if (card) minors.push(card);
  }
}

const majors = Object.entries(MAJOR_DATA).map(([deckId, data]) => buildMajorCard(deckId, data));

function serializeCard(c) {
  const lines = [];
  lines.push('  {');
  lines.push(`    id: '${c.id}',`);
  lines.push(`    deckId: '${c.deckId}',`);
  lines.push(`    nameCn: '${c.nameCn}',`);
  lines.push(`    nameEn: '${c.nameEn}',`);
  lines.push(`    arcana: '${c.arcana}',`);
  lines.push(`    number: ${c.number},`);
  lines.push(`    keywords: ${JSON.stringify(c.keywords)},`);
  lines.push(`    uprightKeywords: ${JSON.stringify(c.uprightKeywords)},`);
  lines.push(`    reversedKeywords: ${JSON.stringify(c.reversedKeywords)},`);
  lines.push(`    oneSentence: ${JSON.stringify(c.oneSentence)},`);
  lines.push(`    uprightMeaning: ${JSON.stringify(c.uprightMeaning)},`);
  lines.push(`    reversedMeaning: ${JSON.stringify(c.reversedMeaning)},`);
  lines.push(`    loveMeaning: ${JSON.stringify(c.loveMeaning)},`);
  lines.push(`    workMeaning: ${JSON.stringify(c.workMeaning)},`);
  lines.push(`    wealthMeaning: ${JSON.stringify(c.wealthMeaning)},`);
  lines.push(`    studyMeaning: ${JSON.stringify(c.studyMeaning)},`);
  lines.push(`    selfMeaning: ${JSON.stringify(c.selfMeaning)},`);
  lines.push(`    misreadings: ${JSON.stringify(c.misreadings)},`);
  lines.push(`    visualOverview: ${JSON.stringify(c.visualOverview)},`);
  lines.push('  }');
  return lines.join('\n');
}

