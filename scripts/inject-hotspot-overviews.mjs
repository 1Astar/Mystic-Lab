const files = [
  'src/knowledge/cards/hotspots/major-arcana.ts',
  'src/knowledge/cards/hotspots/swords-minor.ts',
  'src/knowledge/cards/hotspots/cups-minor.ts',
  'src/knowledge/cards/hotspots/wands-minor.ts',
  'src/knowledge/cards/hotspots/pentacles-minor.ts',
];

function escapeTs(s) {
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

let injected = 0;
let skipped = 0;

for (const rel of files) {
  const path = join(root, rel);
  let text = readFileSync(path, 'utf8');
  const ids = [...text.matchAll(/deckId:\s*'([^']+)'/g)].map((m) => m[1]);

  for (const id of ids) {
    const overview = OVERVIEWS[id];
    if (!overview) continue;

    const blockRe = new RegExp(
      `(deckId:\\s*'${id}',\\s*)(overview:\\s*'[^']*',\\s*)?(hotspots:)`,
      'm',
    );
    const m = text.match(blockRe);
    if (!m) {
      console.warn('block not found', id);
      continue;
    }
    if (m[2]) {
      // replace existing overview (keep swords etc unless we want overwrite — overwrite only if in map for majors missing)
      skipped += 1;
      continue;
    }
    const insert = `overview:\n      '${escapeTs(overview)}',\n    `;
    text = text.replace(blockRe, `$1${insert}$3`);
    injected += 1;
  }

  writeFileSync(path, text, 'utf8');
  console.log('updated', rel);
}

