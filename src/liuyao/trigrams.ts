export function hexagramFormModern(
  name: string,
  fallbackGist: string,
  upper: Trigram,
  lower: Trigram,
): string {
  return (
    HEXAGRAM_FORM_MODERN[name] ??
    (fallbackGist ||
      `${upper.nature}在上、${lower.nature}在下叠合，传统定名为「${name}」。`)
  );
}

export function hexagramNameWhy(name: string, upper: Trigram, lower: Trigram): string {
  return (
    HEXAGRAM_NAME_WHY[name] ??
    `上${upper.nature}（${upper.id}）下${lower.nature}（${lower.id}）叠合，传统定名为「${name}」。`
  );
}
