const BITS_TO_TRIGRAM: Record<string, TrigramId> = {
  '111': '乾',
  '110': '兑',
  '101': '离',
  '100': '震',
  '011': '巽',
  '010': '坎',
  '001': '艮',
  '000': '坤',
};

export function trigramFromBits(bits: [0 | 1, 0 | 1, 0 | 1]): Trigram {
  const key = bits.join('');
  return TRIGRAMS[BITS_TO_TRIGRAM[key] ?? '坤'];
}

export const TRIGRAM_ORDER: TrigramId[] = ['乾', '兑', '离', '震', '巽', '坎', '艮', '坤'];

export function hexagramNameWhy(name: string, upper: Trigram, lower: Trigram): string {
  return (
    HEXAGRAM_NAME_WHY[name] ??
    `上${upper.nature}（${upper.id}）下${lower.nature}（${lower.id}）叠合，传统定名为「${name}」。`
  );
}
