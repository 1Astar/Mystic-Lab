    '1) overview（核心结论，先给）：2–4句。点明：是否精神内耗/疲惫；阻碍更偏情绪·理想化·过往羁绊，还是外部环境；『先冷静休息，再做决定，别在情绪上头时冲动离职』。可点花色气质（宝剑=理智/心力，圣杯=情绪/安全感）；若无星币、无大冲击牌，可写纠结不在钱多钱少或天灾人祸。',
    '2) reason（真相）：紧扣「情况」位牌。不是任务清单有多繁重，而是『心太累了』——大脑与情绪超负荷，更像需要休养，而不是有能量去战斗。insight 5–8句。',
    '3) leave_path：必须分「前期 / 中期 / 后期」（或前段/中后段）。前期：放松、补觉、疗愈；中期：可能迷茫、目标未明、空虚；后期：逐渐回到熟悉/安全节奏或回看初心。可点「不会立刻崩盘，但要预留情绪平复期」。insight 6–12句；可参考整盘，不唯绑定牌百科。',
    '4) stay_path：若绑定牌在「阻碍」位或偏理想化，写『温水煮青蛙』+ 不断给留下找理由（以后会好/领导不错/项目有希望）+ 理想与现实落差；三个月后仍可能内耗并懊恼错过离开时机。insight 6–10句。',
    '5) risk：①情绪化决策（赌气或一时感动）；②盲目乐观/画饼（过度理想化）；③断崖式撤退（不辞而别、切断交接）——应用体面、温和处理旧事务与关系。标「情况：」「阻碍：」。insight 5–8句。',
